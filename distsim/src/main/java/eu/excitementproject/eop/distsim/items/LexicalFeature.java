package eu.excitementproject.eop.distsim.items;

/**
 * An element which is based on a given lexical string
 * 
 *  @author Meni Adler
 * @since 20/06/2012
 * 
 * 
 * Thread-safe
 */
public class LexicalFeature extends StringBasedFeature {

	private static final long serialVersionUID = 1L;

	public LexicalFeature(String text) {
		super(text);
	}

	public LexicalFeature(String text, AggregatedContext context) {
		super(text, context);
	}

	public LexicalFeature(String text, int id, long count) {
		super(text,id,count);

	}
	
	public LexicalFeature(String text, AggregatedContext context, int id, long count) {
		super(text,context,id,count);
	}	
}
